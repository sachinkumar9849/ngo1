    document.addEventListener('DOMContentLoaded', function () {
        const supportButtons = document.querySelectorAll('.btn-support');
        const supportModal = document.getElementById('supportModal');
        if (supportButtons && supportModal) {
            const bsModal = new bootstrap.Modal(supportModal);
            supportButtons.forEach(btn => {
                btn.addEventListener('click', function (e) {
                    // Prevent scroll jumps if it is a hash link
                    if (btn.getAttribute('href') === '#get-support') {
                        e.preventDefault();
                        bsModal.show();
                    }
                });
            });
        }
    });
</script>

